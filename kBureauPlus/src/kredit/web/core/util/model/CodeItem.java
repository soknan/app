package kredit.web.core.util.model;

/**
 * @author vathenan
 *
 */
public class CodeItem {
	int id;
	String code = "";
	String description = "";
	int superId;
	String superCode = "";
	String superDescription = "";
	int value;
	
	/**
	 * @return the code
	 */
	public String getCode() {
		return code;
	}
	/**
	 * @param code the code to set
	 */
	public void setCode(String code) {
		this.code = code;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	
	
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the superId
	 */
	public int getSuperId() {
		return superId;
	}
	/**
	 * @param superId the superId to set
	 */
	public void setSuperId(int superId) {
		this.superId = superId;
	}
	/**
	 * @return the superCode
	 */
	public String getSuperCode() {
		return superCode;
	}
	/**
	 * @param superCode the superCode to set
	 */
	public void setSuperCode(String superCode) {
		this.superCode = superCode;
	}
	/**
	 * @return the superDescription
	 */
	public String getSuperDescription() {
		return superDescription;
	}
	/**
	 * @param superDescription the superDescription to set
	 */
	public void setSuperDescription(String superDescription) {
		this.superDescription = superDescription;
	}
	/**
	 * @return the value
	 */
	public int getValue() {
		return value;
	}
	/**
	 * @param value the value to set
	 */
	public void setValue(int value) {
		this.value = value;
	}
}
