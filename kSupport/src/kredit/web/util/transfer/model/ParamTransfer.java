package kredit.web.util.transfer.model;

import java.util.HashSet;
import java.util.Set;

import kredit.web.core.util.model.CodeItem;

public class ParamTransfer {
	String filter;
	
	Set<CodeItem> communes;
	Set<CodeItem> villages;
	Set<CodeItem> products;
	
	public String getFilter() {
		return filter;
	}
	
	public void setFilter(String filter) {
		this.filter = filter;
	}
	
	public Set<CodeItem> getCommunes() {
		if (communes == null)
			communes = new HashSet<CodeItem>();
		return communes;
	}
	
	public void setCommunes(Set<CodeItem> communes) {
		this.communes = communes;
	}
	
	public Set<CodeItem> getVillages() {
		if (villages == null)
			villages = new HashSet<CodeItem>();
		return villages;
	}
	
	public void setVillages(Set<CodeItem> villages) {
		this.villages = villages;
	}
	
	public Set<CodeItem> getProducts() {
		if (products == null)
			products = new HashSet<CodeItem>();
		return products;
	}
	
	public void setProducts(Set<CodeItem> products) {
		this.products = products;
	}
	
}
