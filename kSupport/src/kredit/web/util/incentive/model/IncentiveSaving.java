package kredit.web.util.incentive.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name="INC_ACMT_SAVING")
public class IncentiveSaving {

	@Id
	Integer id;
	
	Integer crdtofcr_id;
	String crdtofcr_name;
	Double act_account;
	Double osnd_balance;
	Double account_open_monthly;
	Double amount_open_monthly;
	Date acmt_date;
	String branch_code;
	int batch_code;
	Date created_date;
	String created_user;
	Date modified_date;
	String modified_user;
	String note;
	
	public Integer getId() {
		return id;
	}
	
	public void setId(Integer id) {
		this.id = id;
	}
	
	public Integer getCrdtofcr_id() {
		return crdtofcr_id;
	}

	public void setCrdtofcr_id(Integer crdtofcr_id) {
		this.crdtofcr_id = crdtofcr_id;
	}

	public String getCrdtofcr_name() {
		return crdtofcr_name;
	}

	public void setCrdtofcr_name(String crdtofcr_name) {
		this.crdtofcr_name = crdtofcr_name;
	}

	public Double getAct_account() {
		return act_account;
	}
	
	public void setAct_account(Double act_account) {
		this.act_account = act_account;
	}
	
	public Double getOsnd_balance() {
		return osnd_balance;
	}
	
	public void setOsnd_balance(Double osnd_balance) {
		this.osnd_balance = osnd_balance;
	}
	
	public Double getAccount_open_monthly() {
		return account_open_monthly;
	}
	
	public void setAccount_open_monthly(Double account_open_monthly) {
		this.account_open_monthly = account_open_monthly;
	}
	
	public Double getAmount_open_monthly() {
		return amount_open_monthly;
	}
	
	public void setAmount_open_monthly(Double amount_open_monthly) {
		this.amount_open_monthly = amount_open_monthly;
	}
	
	public Date getAcmt_date() {
		return acmt_date;
	}
	
	public void setAcmt_date(Date acmt_date) {
		this.acmt_date = acmt_date;
	}
	
	public String getBranch_code() {
		return branch_code;
	}
	
	public void setBranch_code(String branch_code) {
		this.branch_code = branch_code;
	}
	
	public int getBatch_code() {
		return batch_code;
	}
	
	public void setBatch_code(int batch_code) {
		this.batch_code = batch_code;
	}
	
	public Date getCreated_date() {
		return created_date;
	}
	
	public void setCreated_date(Date created_date) {
		this.created_date = created_date;
	}
	
	public String getCreated_user() {
		return created_user;
	}
	
	public void setCreated_user(String created_user) {
		this.created_user = created_user;
	}
	
	public Date getModified_date() {
		return modified_date;
	}
	
	public void setModified_date(Date modified_date) {
		this.modified_date = modified_date;
	}
	
	public String getModified_user() {
		return modified_user;
	}
	
	public void setModified_user(String modified_user) {
		this.modified_user = modified_user;
	}
	
	public String getNote() {
		return note;
	}
	
	public void setNote(String note) {
		this.note = note;
	}
	
}
