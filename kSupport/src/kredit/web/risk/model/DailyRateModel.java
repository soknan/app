package kredit.web.risk.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Table;

@Entity
@Table(name = "CLR_DAILY_RATING")
public class DailyRateModel{
	private static final long serialVersionUID = 1L;

	String branch_code;
	String branch_name;
	String cif;
	String cust_name;
	Integer num_account;
	String initial_class;
	String initial_type;	
	Double daily_deposit;
	String daily_status;
	Date value_date;
	Integer no;
	
	String create_by;	
	Date create_on;	
	String change_by;	
	Date change_on;
	
	
	public String getBranch_name() {
		return branch_name;
	}
	public void setBranch_name(String branch_name) {
		this.branch_name = branch_name;
	}

	public Integer getNo() {
		return no;
	}
	public void setNo(Integer no) {
		this.no = no;
	}
	public Date getValue_date() {
		return value_date;
	}
	public void setValue_date(Date value_date) {
		this.value_date = value_date;
	}
	public String getBranch_code() {
		return branch_code;
	}
	public void setBranch_code(String branch_code) {
		this.branch_code = branch_code;
	}
	public String getCif() {
		return cif;
	}
	public void setCif(String cif) {
		this.cif = cif;
	}
	public String getCust_name() {
		return cust_name;
	}
	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
	public Integer getNum_account() {
		return num_account;
	}
	public void setNum_account(Integer num_account) {
		this.num_account = num_account;
	}	
	public String getInitial_class() {
		return initial_class;
	}
	public void setInitial_class(String initial_class) {
		this.initial_class = initial_class;
	}
	public String getInitial_type() {
		return initial_type;
	}
	public void setInitial_type(String initial_type) {
		this.initial_type = initial_type;
	}
	public Double getDaily_deposit() {
		return daily_deposit;
	}
	public void setDaily_deposit(Double daily_deposit) {
		this.daily_deposit = daily_deposit;
	}
	public String getDaily_status() {
		return daily_status;
	}
	public void setDaily_status(String daily_status) {
		this.daily_status = daily_status;
	}
	
	public String getCreate_by() {
		return create_by;
	}
	public void setCreate_by(String create_by) {
		this.create_by = create_by;
	}
	public Date getCreate_on() {
		return create_on;
	}
	public void setCreate_on(Date create_on) {
		this.create_on = create_on;
	}
	public String getChange_by() {
		return change_by;
	}
	public void setChange_by(String change_by) {
		this.change_by = change_by;
	}
	public Date getChange_on() {
		return change_on;
	}
	public void setChange_on(Date change_on) {
		this.change_on = change_on;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
}
